//Unary Operators Are USe For Increment OR Decrement

public class UnaryOperators {

    public static void main(String[] args) {

        int a = 10;

        System.out.println(++a);
        System.out.println(a++);
        System.out.println(a);

        System.out.println(--a);
        System.out.println(a--);
        System.out.println(a);

    }
}